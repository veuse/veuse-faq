<h1>Theme options</h1>
<p>You must go through the theme options setup before the theme will work properly. You find the <i>Theme Options</i> under the Appearance menu in your wp-admin.</p>

<p>Most of the options are either self-explanatory, or have a description telling what it does.</p>

<h3>Here is a short summary of the options</h3>
<ul>
	<li>General settings</li>
	<li>Header settings</li>
	<li>Footer settings</li>
	<li>Blog settings</li>
	<li>Typography</li>
	<li>Colors and styles</li>
</ul>

